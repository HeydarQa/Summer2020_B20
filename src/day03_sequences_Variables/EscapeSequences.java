package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello everyone \n\tMy name is Cybertek"); /*
         \n setiri yariya bolur yeni setirden basladir -  appends new line
         \t tab boslugu yaradir.obzasdan baslamag ucun - create new line with tab*/
        System.out.println("\n\n\n\t\t\t\\I love Java");
        System.out.println("\\\\");  // "\" isareni print elemey ucun \\ yazmag lazimdi

        System.out.println("My name is \'Heydar\'"); // \' for write single quote
        System.out.println("My name is 'Heydar'");

        System.out.println("My favorite TV-series: \"Game of Thrones\"");
        System.out.println(("\tMy Name is\'Heydar\'"));




    }

}


